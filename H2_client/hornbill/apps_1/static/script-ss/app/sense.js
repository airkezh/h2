/*common*/

function loadSense(){
	var head = document.head  || document.getElementsByTagName('head')[0] || document.documentElement

	var l = document.createElement('script')
	l.type = 'text/javascript'
	l.src = 'http://pages.w.meilishuo.com/sense/?url=' + encodeURIComponent(window.location.href) 
	head.appendChild(l)
}

window.setTimeout(loadSense , 100)
