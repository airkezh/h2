$("#btn_yuyue").on("click", function() {
	user.realname = $('#uname').val();
	user.phone = $('#phone').val();
	user.yzm = $('#yzm').val();
	user.shoppe = $('#shoppe').val();
	user.dates = $('#data_option').val();
	if (user.realname == '') {
		alert("请输入姓名。");
		return;
	}
	if (user.phone == "") {
		alert("请输入手机号码。");
		return;
	} else if (!/^(((13[0-9]{1})|(14[0-9]{1})|(15[0-9]{1})|(17[0-9]{1})|(18[0-9]{1})|(19[0-9]{1}))+\d{8})$/.test(user.phone)) {
		alert("请输入正确的手机号码。");
		return;
	}
	if (user.yzm == '') {
		alert("请输入验证码。");
		return;
	}
	if (user.city == '--选择--') {
		alert("请选择城市。");
		return;
	}
	if (user.shoppe == '--选择--') {
		alert("请选择柜台。");
		return;
	}
	if (user.dates == '') {
		alert("请选择日期。");
		return;
	}
	user.code = newData[user.cityIndex].code[document.getElementById('shoppe').selectedIndex - 1];
	$.ajax({
		type: 'post',
		url: '/aj/single_aj/olay_yuyue/olay_yy',
		data: {
			"activity": "olay_record_2",
			"name": user.realname,
			"phone": user.phone,
			"date": user.dates,
			"shoppe": user.shoppe,
			"storeCode": user.code,
			"code": user.yzm,
			"city": user.city
		},
		success: function(res) {
			res = JSON.parse(res);
			var error_code = res.error_code;
			if (error_code == 0) {
				alert("预约成功");
				window.location.href = "/activity/olay/olay_yuyue_success/";
			} else if (error_code == "400609") {
				alert("您已经预约过，请不要重复预约");
				window.location.href = "/activity/olay/olay_yuyue_success/";
			} else {
				alert("预约失败");
			}
		}
	});
});

var isSending = false;
$("#send").on("click", function() {
	user.phone = $('#phone').val();
	if (user.phone == "") {
		alert("请输入手机号码。");
		return;
	} else if (!/^(((13[0-9]{1})|(14[0-9]{1})|(15[0-9]{1})|(17[0-9]{1})|(18[0-9]{1})|(19[0-9]{1}))+\d{8})$/.test(user.phone)) {
		alert("请输入正确的手机号码。");
		return;
	}
	if (isSending) {
		return;
	}
	isSending = true;
	var count = 60,
		self = $(this);
	$.ajax({
		type: 'get',
		url: '/aj/single_aj/olay_yuyue/olay_sms_op',
		data: {
			"op": "send",
			"phone": user.phone
		},
		success: function(data) {
			data = JSON.parse(data);
			var error_code = data.error_code;
			if (error_code == 0) {
				alert('短信发送成功，请注意查收短信。60秒后可重新发送验证码');
				self.html(count + '秒');
				window.timeOut = setInterval(function() {
					count--;
					if (count < 0) {
						clearInterval(window.timeOut);
						self.html('获取验证码');
						isSending = false;
						return;
					}
					self.html(count + '秒');
				}, 1000);
			} else {
				clearInterval(window.timeOut);
				self.html('获取验证码');
				isSending = false;
				alert(data.message);
			}
		}
	});
});
